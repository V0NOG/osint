// All UN-recognized countries with risk data.
// Detailed geopolitical data for the 20 highest-focus countries;
// standard entries for the rest.

export type RiskLevel = 'critical' | 'high' | 'elevated' | 'moderate' | 'low' | 'minimal'

export interface CountrySeed {
  name: string
  slug: string
  iso2: string
  iso3: string
  capital: string
  region: string
  riskLevel: RiskLevel
  riskScore: number
  population?: bigint
  gdp?: number
  description?: string
}

function c(
  name: string, iso2: string, iso3: string, capital: string, region: string,
  riskLevel: RiskLevel, riskScore: number,
  pop?: number, gdp?: number, desc?: string
): CountrySeed {
  return {
    name,
    slug: name.toLowerCase().replace(/[^a-z0-9]+/g, '-').replace(/^-|-$/g, ''),
    iso2, iso3, capital, region, riskLevel, riskScore,
    population: pop ? BigInt(pop) : undefined,
    gdp,
    description: desc,
  }
}

